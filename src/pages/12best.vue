              <h2 id="how-to">
                How to Install a Free Shopify Template (Step-by-Step)
              </h2>
              <p>
                Installing one of the best free Shopify templates takes less
                than five minutes. Here is how:
              </p>
              <p>
                <strong>Step 1: Navigate to the</strong>
                <a
                  href="https://themes.shopify.com/themes/"
                  target="blank"
                  class="cmt-link"
                >
                  Shopify Theme Store.</a
                >
                From your Shopify admin, go to Online Store > Themes > Visit
                Theme Store. You will land on the official theme marketplace.
              </p>
              <p>
                <strong>Step 2: Filter by Free.</strong> On the left sidebar,
                select the Price filter and choose Free. You will see all
                currently available free templates, including every theme on
                this list.
              </p>
              <p>
                <strong>Step 3: Preview before you commit.</strong> Click any
                theme to open its detail page. Use the desktop and mobile
                preview toggles to see exactly how it will look across devices.
                Most templates also have live demo stores you can explore.
              </p>
              <p>
                <strong>Step 4: Add the theme to your store.</strong> Click the
                ‘Add’ button on the theme detail page. Shopify will add it to
                your theme library under Online Store > Themes. It will not go
                live automatically — your current theme stays active.
              </p>
              <p>
                <strong>Step 5: Customize using the editor.</strong> Click
                Customize next to the new theme. Use Shopify’s drag-and-drop
                sections editor to upload your logo, set your brand colors, add
                your products to homepage sections, and configure navigation.
              </p>
              <p>
                <strong>Step 6: Publish when ready.</strong> Once you are happy
                with the customization, click Actions > Publish. Your store will
                switch to the new theme. You can always revert to your previous
                theme instantly — Shopify stores your last three theme versions.
              </p>
